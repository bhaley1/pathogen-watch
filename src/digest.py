_BADGE_FOR_EVENT = {
    EventType.NEW_HUMAN_CLUSTER: ("badge-new", "New"),
    EventType.NEW_HUMAN_IN_CLUSTER: ("badge-grow", "Human added"),
    EventType.NEW_NONHUMAN_NEAR_HUMAN: ("badge-mixed", "Nonhuman added"),
}


def _h(s) -> str:
    """HTML-escape, treating None as empty."""
    return html.escape("" if s is None else str(s))


def _events_table(events: list[Event]) -> str:
    if not events:
        return '<p class="empty">No new alert events.</p>'
    rows = []
    for ev in events[:config.MAX_CLUSTERS_PER_SECTION]:
        cls, label = _BADGE_FOR_EVENT[ev.event_type]
        new_iso_html = ""
        if ev.new_pdt_accs:
            shown = ev.new_pdt_accs[:5]
            extra = "" if len(ev.new_pdt_accs) <= 5 else f" (+{len(ev.new_pdt_accs)-5})"
            new_iso_html = ", ".join(f"<code>{_h(p)}</code>" for p in shown) + extra
        rows.append(
            f"<tr>"
            f"<td><span class='badge {cls}'>{label}</span></td>"
            f"<td><a href='{_h(_cluster_url(ev.pds_acc))}'><code>{_h(ev.pds_acc)}</code></a></td>"
            f"<td>{_h(ev.summary)}</td>"
            f"<td>{new_iso_html}</td>"
            f"</tr>"
        )
    return (
        "<table><thead><tr>"
        "<th>Type</th><th>Cluster</th><th>Summary</th><th>New isolates</th>"
        "</tr></thead><tbody>"
        + "".join(rows) + "</tbody></table>"
    )


def _mixed_table(mixed: list[Cluster]) -> str:
    if not mixed:
        return '<p class="empty">No active mixed clusters in the recent window.</p>'
    rows = []
    for c in mixed[:config.MAX_CLUSTERS_PER_SECTION]:
        tight = c.tightest_human_min_same
        tight_str = f"≤{tight}" if tight is not None else "—"
        rows.append(
            f"<tr>"
            f"<td><a href='{_h(_cluster_url(c.pds_acc))}'><code>{_h(c.pds_acc)}</code></a></td>"
            f"<td>{c.n_human}</td>"
            f"<td>{c.n_nonhuman}</td>"
            f"<td>{tight_str}</td>"
            f"<td>{_h(c.latest_collection or '—')}</td>"
            f"<td>{_h(_fmt_sources(c))}</td>"
            f"<td>{_h(_fmt_countries(c))}</td>"
            f"</tr>"
        )
    return (
        "<table><thead><tr>"
        "<th>Cluster</th><th>Hu</th><th>Non-Hu</th><th>Tightest SNP gap</th>"
        "<th>Latest</th><th>Nonhuman sources</th><th>Geography</th>"
        "</tr></thead><tbody>"
        + "".join(rows) + "</tbody></table>"
    )


def _neighbors_table(nns: list[CrossClusterNearNeighbor], threshold: int) -> str:
    if not nns:
        return ""
    rows = []
    for n in nns[:config.MAX_ISOLATES_PER_CLUSTER]:
        iso = n.isolate
        rows.append(
            f"<tr>"
            f"<td><a href='{_h(_isolate_url(iso.pdt_acc))}'><code>{_h(iso.pdt_acc)}</code></a></td>"
            f"<td>{_h(iso.min_diff)}</td>"
            f"<td>{_h(iso.isolation_source or iso.host or '—')}</td>"
            f"<td><code>{_h(iso.pds_acc)}</code></td>"
            f"<td>{_h(iso.collection_date or '—')}</td>"
            f"<td>{_h(iso.geo_loc_name or '—')}</td>"
            f"</tr>"
        )
    return (
        f"<h3><span class='badge badge-near'>≤{threshold} SNPs</span> "
        f"<span class='count'>{len(nns)} nonhuman isolate(s)</span></h3>"
        "<table><thead><tr>"
        "<th>Isolate</th><th>min_diff</th><th>Source</th><th>Own cluster</th>"
        "<th>Collected</th><th>Geography</th>"
        "</tr></thead><tbody>" + "".join(rows) + "</tbody></table>"
    )


def render_html(
    run_date: date,
    events_by_pathogen: dict[str, list[Event]],
    mixed_clusters_by_pathogen: dict[str, list[Cluster]],
    near_neighbors_by_pathogen: dict[str, dict[int, list[CrossClusterNearNeighbor]]],
) -> str:
    total_events = sum(len(v) for v in events_by_pathogen.values())
    parts: list[str] = []
    parts.append(f"<!doctype html><html><head><meta charset='utf-8'>"
                 f"<title>Pathogen Detection Digest — {run_date.isoformat()}</title>"
                 f"<style>{_HTML_STYLE}</style></head><body>")
    parts.append(f"<h1>NCBI Pathogen Detection — Daily Digest</h1>")
    parts.append(
        f"<p class='meta'>{run_date.strftime('%A, %B %d, %Y')} · "
        f"<strong>{total_events}</strong> new event(s) across "
        f"{len(config.PATHOGENS)} pathogens · "
        f"recent window: {config.RECENT_WINDOW_DAYS} days</p>"
    )

    for pathogen in config.PATHOGENS:
        events = events_by_pathogen.get(pathogen, [])
        mixed = mixed_clusters_by_pathogen.get(pathogen, [])
        neighbors = near_neighbors_by_pathogen.get(pathogen, {})

        parts.append(f"<h2>{_h(pathogen)} "
                     f"<span class='count'>· {len(events)} event(s)</span></h2>")

        parts.append(f"<h3>Alerts</h3>")
        parts.append(_events_table(events))

        parts.append(f"<h3>Active mixed clusters <span class='count'>"
                     f"({len(mixed)} in last {config.RECENT_WINDOW_DAYS} days)</span></h3>")
        parts.append(_mixed_table(mixed))

        nn_total = sum(len(v) for v in neighbors.values())
        if nn_total:
            parts.append(f"<h3>Tight cross-cluster nonhuman neighbors "
                         f"<span class='count'>(early-warning)</span></h3>")
            for thresh in config.SNP_THRESHOLDS:
                parts.append(_neighbors_table(neighbors.get(thresh, []), thresh))

    parts.append(
        "<div class='footer'>"
        "Generated by <code>pathogen-alerts</code>. "
        "Source: <a href='https://www.ncbi.nlm.nih.gov/pathogens/'>"
        "NCBI Pathogen Detection</a>. "
        "Thresholds are upper bounds derived from per-isolate <code>min_same</code> / "
        "<code>min_diff</code>; verify clusters of interest directly at NCBI."
        "</div></body></html>"
    )
    return "".join(parts)
