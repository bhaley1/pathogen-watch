"""Render daily digest email.

Two outputs:
  - Plain-text summary suitable for email body fallback or terminal output
  - HTML email with a clean editorial layout

Sections (per pathogen, in order):
  1. New alerts (events from diff) — the action items
  2. Active mixed clusters (human + nonhuman) — surveillance overview
  3. Tight cross-cluster near-neighbors — early warning

Designed to be glanceable on mobile and informative on desktop.
"""

from __future__ import annotations

import html
import logging
from collections import defaultdict
from datetime import date, datetime
from typing import Iterable

from . import config
from .cluster import Cluster, CrossClusterNearNeighbor
from .state import Event, EventType

log = logging.getLogger(__name__)


EVENT_LABEL = {
    EventType.NEW_HUMAN_CLUSTER: "New human cluster",
    EventType.NEW_HUMAN_IN_CLUSTER: "Human case added to cluster",
    EventType.NEW_NONHUMAN_NEAR_HUMAN: "Nonhuman isolate added to human cluster",
}


def _cluster_url(pds: str) -> str:
    return f"https://www.ncbi.nlm.nih.gov/pathogens/isolates/#{pds}"


def _isolate_url(pdt: str) -> str:
    return f"https://www.ncbi.nlm.nih.gov/pathogens/isolates/#{pdt}"


def _fmt_sources(c: Cluster) -> str:
    src = sorted(c.nonhuman_sources)
    if not src:
        return "—"
    if len(src) > 4:
        return ", ".join(src[:4]) + f" (+{len(src)-4} more)"
    return ", ".join(src)


def _fmt_countries(c: Cluster) -> str:
    countries = sorted(c.countries)
    if not countries:
        return "—"
    if len(countries) > 5:
        return ", ".join(countries[:5]) + f" (+{len(countries)-5})"
    return ", ".join(countries)


# ---------------------------------------------------------------------------
# Plain text
# ---------------------------------------------------------------------------

def render_text(
    run_date: date,
    events_by_pathogen: dict[str, list[Event]],
    mixed_clusters_by_pathogen: dict[str, list[Cluster]],
    near_neighbors_by_pathogen: dict[str, dict[int, list[CrossClusterNearNeighbor]]],
) -> str:
    lines: list[str] = []
    total_events = sum(len(v) for v in events_by_pathogen.values())
    lines.append(f"NCBI Pathogen Detection — Daily Alert Digest")
    lines.append(f"Run date: {run_date.isoformat()}")
    lines.append(f"Total new events: {total_events}")
    lines.append("=" * 72)

    for pathogen in config.PATHOGENS:
        events = events_by_pathogen.get(pathogen, [])
        mixed = mixed_clusters_by_pathogen.get(pathogen, [])
        neighbors = near_neighbors_by_pathogen.get(pathogen, {})

        lines.append("")
        lines.append(f"### {pathogen}")
        lines.append("-" * 72)

        # Events
        if events:
            lines.append(f"NEW EVENTS ({len(events)}):")
            for ev in events[:config.MAX_CLUSTERS_PER_SECTION]:
                lines.append(f"  [{EVENT_LABEL[ev.event_type]}] {ev.summary}")
                lines.append(f"    Cluster: {_cluster_url(ev.pds_acc)}")
                if ev.new_pdt_accs:
                    shown = ev.new_pdt_accs[:5]
                    extra = "" if len(ev.new_pdt_accs) <= 5 else f" (+{len(ev.new_pdt_accs)-5} more)"
                    lines.append(f"    New isolates: {', '.join(shown)}{extra}")
        else:
            lines.append("No new alert events.")

        # Mixed clusters
        if mixed:
            lines.append("")
            lines.append(f"ACTIVE MIXED CLUSTERS ({len(mixed)} shown, most-recent first):")
            for c in mixed[:config.MAX_CLUSTERS_PER_SECTION]:
                tight = c.tightest_human_min_same
                tight_str = f"≤{tight} SNPs" if tight is not None else "n/a"
                lines.append(
                    f"  {c.pds_acc}: {c.n_human} human / {c.n_nonhuman} nonhuman; "
                    f"tightest human gap: {tight_str}; "
                    f"latest: {c.latest_collection or 'n/a'}; "
                    f"sources: {_fmt_sources(c)}; "
                    f"geo: {_fmt_countries(c)}"
                )

        # Near-neighbors
        for thresh in config.SNP_THRESHOLDS:
            nns = neighbors.get(thresh, [])
            if not nns:
                continue
            lines.append("")
            lines.append(f"TIGHT NONHUMAN CROSS-CLUSTER NEIGHBORS (≤{thresh} SNPs): {len(nns)}")
            for n in nns[:config.MAX_ISOLATES_PER_CLUSTER]:
                iso = n.isolate
                lines.append(
                    f"  {iso.pdt_acc} ({iso.isolation_source or iso.host or '?'}): "
                    f"min_diff={iso.min_diff}; cluster {iso.pds_acc}; "
                    f"collected {iso.collection_date or '?'}; geo {iso.geo_loc_name or '?'}"
                )

    lines.append("")
    lines.append("=" * 72)
    lines.append("Generated by pathogen-alerts. Source: NCBI Pathogen Detection.")
    return "\n".join(lines)


